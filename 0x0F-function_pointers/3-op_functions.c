/**
 * op_add - a function that adds two integers
 * @a: the first integer
 * @b: the second integer
 * Return: returns the result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that evaluates the difference between two integers
 * @a: the first integer
 * @b: the second integer
 * Return: returns the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that multiplies two integers
 * @a: the first integer
 * @b: the second integer
 * Return: returns the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that divides two integers
 * @a: the first integer
 * @b: the second integer
 * Return: returns the result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that divides 2 integers and returns the remainder
 * @a: the first integer
 * @b: the second integer
 * Return: returns the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
